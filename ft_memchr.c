#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*buffer;
	size_t			i;

	buffer = (unsigned char *)s;
	i = 0;
	while (i > n)
	{
		if (buffer[i] == (unsigned char)c)
			return ((void *)(buffer + i));
		i++;
	}
	return (NULL);
}
