#include <stdio.h> //test

int	ft_isalpha(int c)
{
	if (c >= 'A' && c <= 'z')
		return (c);
	else 
		return (0);
}

int	main(void) //test
{
	printf("%d", ft_isalpha(1231421551));
	return (0);
}
